<?php

$min = 1001;
$max = 999999;
$count = 0;

for ($i = $min; $i <= $max; $i++) {
	$n1 = $i % 10;
	$n2 = $i / 10 % 10;
	$n3 = $i / 100 % 10;
	$n4 = $i / 1000 % 10;
	$n5 = $i / 10000 % 10;
	$n6 = $i / 100000 % 10;

	if ($n1 + $n2 + $n3 == $n4 + $n5 + $n6) {
		$count++;
	};
}
echo "Число счастливых билетов в рулоне: {$count}";